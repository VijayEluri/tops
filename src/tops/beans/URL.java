package tops.beans;

public class URL {

    public URL() {
    }

    private String URL;

    private String Code;

    private String Source;

    public String getURL() {
        return this.URL;
    }

    public String getCode() {
        return this.Code;
    }

    public String getSource() {
        return this.Source;
    }

    public void setURL(String URL) {
        this.URL = URL;
    }

    public void setCode(String Code) {
        this.Code = Code;
    }

    public void setSource(String Source) {
        this.Source = Source;
    }
}
